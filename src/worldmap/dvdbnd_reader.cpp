// ── crypto ────────────────────────────────────────────────────────────────────
// RSA public-key op (m = c^e mod n) over every kModLen-byte cipher block of `enc`,
// dropping each block's leading byte → BHD5 plaintext. Windows CNG does the raw
// modexp (BCRYPT_PAD_NONE). Empty on any failure.
std::vector<uint8_t> rsa_decrypt(const std::vector<uint8_t> &enc, const ArchiveKey &k)
{
    if (enc.empty() || (enc.size() % kModLen) != 0)
        return {};

    std::vector<uint8_t> blob(sizeof(BCRYPT_RSAKEY_BLOB) + k.explen + kModLen);
    auto *hdr        = reinterpret_cast<BCRYPT_RSAKEY_BLOB *>(blob.data());
    hdr->Magic       = BCRYPT_RSAPUBLIC_MAGIC;
    hdr->BitLength   = kModLen * 8;
    hdr->cbPublicExp = k.explen;
    hdr->cbModulus   = kModLen;
    hdr->cbPrime1    = 0;
    hdr->cbPrime2    = 0;
    std::memcpy(blob.data() + sizeof(BCRYPT_RSAKEY_BLOB), k.exp, k.explen);
    std::memcpy(blob.data() + sizeof(BCRYPT_RSAKEY_BLOB) + k.explen, k.mod, kModLen);

    BCRYPT_ALG_HANDLE alg = nullptr;
    if (!BCRYPT_SUCCESS(BCryptOpenAlgorithmProvider(&alg, BCRYPT_RSA_ALGORITHM, nullptr, 0)))
        return {};
    std::vector<uint8_t> out;
    BCRYPT_KEY_HANDLE    key = nullptr;
    if (BCRYPT_SUCCESS(BCryptImportKeyPair(alg, nullptr, BCRYPT_RSAPUBLIC_BLOB, &key, blob.data(),
                                           static_cast<ULONG>(blob.size()), 0)))
    {
        out.reserve(enc.size() / kModLen * (kModLen - 1));
        uint8_t buf[kModLen];
        bool    fail = false;
        for (size_t off = 0; off < enc.size(); off += kModLen)
        {
            ULONG cb = 0;
            NTSTATUS s =
                BCryptEncrypt(key, const_cast<PUCHAR>(&enc[off]), kModLen, nullptr, nullptr, 0, buf,
                              kModLen, &cb, BCRYPT_PAD_NONE);
            if (!BCRYPT_SUCCESS(s) || cb != kModLen)
            {
                fail = true;
                break;
            }
            out.insert(out.end(), buf + 1, buf + kModLen);  // drop the leading byte
        }
        if (fail)
            out.clear();
        BCryptDestroyKey(key);
    }
    BCryptCloseAlgorithmProvider(alg, 0);
    return out;
}

// AES-128-ECB (no padding) decrypt the listed byte ranges of `data` in place, using the
// 16-byte key + Range[] stored at `aesOff` in the decrypted BHD5. Rare (regulation etc.);
// asset files have aesOff == 0 and skip this entirely.
void apply_aes(std::vector<uint8_t> &data, const std::vector<uint8_t> &bhd, uint64_t aesOff)
{
    if (aesOff + 20 > bhd.size())
        return;
    const uint8_t *keyb = &bhd[aesOff];
    int32_t        rangeCount = 0;
    std::memcpy(&rangeCount, &bhd[aesOff + 16], 4);
    if (rangeCount <= 0 || rangeCount > 64)
        return;

    BCRYPT_ALG_HANDLE alg = nullptr;
    if (!BCRYPT_SUCCESS(BCryptOpenAlgorithmProvider(&alg, BCRYPT_AES_ALGORITHM, nullptr, 0)))
        return;
    BCryptSetProperty(alg, BCRYPT_CHAINING_MODE, (PUCHAR)BCRYPT_CHAIN_MODE_ECB,
                      sizeof(BCRYPT_CHAIN_MODE_ECB), 0);
    BCRYPT_KEY_HANDLE hk = nullptr;
    if (BCRYPT_SUCCESS(BCryptGenerateSymmetricKey(alg, &hk, nullptr, 0, (PUCHAR)keyb, 16, 0)))
    {
        size_t rp = aesOff + 20;
        for (int32_t i = 0; i < rangeCount && rp + 16 <= bhd.size(); ++i, rp += 16)
        {
            int64_t start = 0, end = 0;
            std::memcpy(&start, &bhd[rp], 8);
            std::memcpy(&end, &bhd[rp + 8], 8);
            if (start < 0 || end < 0 || start == end)
                continue;
            if ((uint64_t)end > data.size() || (uint64_t)start >= data.size())
                continue;
            ULONG count = (ULONG)(end - start);
            if (count % 16)  // ECB needs whole blocks; ER ranges are 16-aligned
                continue;
            ULONG cb = 0;
            BCryptDecrypt(hk, &data[start], count, nullptr, nullptr, 0, &data[start], count, &cb, 0);
        }
        BCryptDestroyKey(hk);
    }
    BCryptCloseAlgorithmProvider(alg, 0);
}

// ── BHD5 parse (Game.EldenRing) ─────────────────────────────────────────────────
bool parse_bhd5(const std::vector<uint8_t> &b, BhdIndex &out)
{
    if (b.size() < 0x20 || !(b[0] == 'B' && b[1] == 'H' && b[2] == 'D' && b[3] == '5'))
        return false;
    auto i32 = [&](size_t p) { int32_t v; std::memcpy(&v, &b[p], 4); return v; };
    auto i64 = [&](size_t p) { int64_t v; std::memcpy(&v, &b[p], 8); return v; };
    auto u32 = [&](size_t p) { uint32_t v; std::memcpy(&v, &b[p], 4); return v; };
    auto u64 = [&](size_t p) { uint64_t v; std::memcpy(&v, &b[p], 8); return v; };

    bool is64 = (b.size() > 0x28) && i32(0x14) == 0 && i32(0x1c) == 0;  // ER → false
    size_t  pos = 0x10;
    int64_t bucketCount, bucketsOffset;
    if (is64) { bucketCount = i64(pos); pos += 8; bucketsOffset = i64(pos); pos += 8; }
    else      { bucketCount = i32(pos); pos += 4; bucketsOffset = i32(pos); pos += 4; }
    if (bucketCount < 0 || bucketCount > 1000000)
        return false;
    // (saltLength + salt at `pos` — not needed; buckets are at bucketsOffset.)

    size_t bp = (size_t)bucketsOffset;
    struct Bk { int32_t cnt; int64_t foff; };
    std::vector<Bk> buckets;
    buckets.reserve((size_t)bucketCount);
    const size_t bucketSz = is64 ? 16 : 8;
    for (int64_t i = 0; i < bucketCount; ++i)
    {
        if (bp + bucketSz > b.size())
            return false;
        int32_t cnt = i32(bp);
        bp += 4;
        if (is64)
            bp += 4;  // unknownFlag == 1
        int64_t foff = is64 ? i64(bp) : (int64_t)i32(bp);
        bp += is64 ? 8 : 4;
        buckets.push_back({cnt, foff});
    }
    for (const Bk &bk : buckets)
    {
        size_t fp = (size_t)bk.foff;
        for (int32_t j = 0; j < bk.cnt; ++j, fp += 40)  // ER FileHeader = 40 bytes
        {
            if (fp + 40 > b.size())
                return false;
            uint64_t h      = u64(fp);
            uint32_t padded = u32(fp + 8);
            // u32 unpaddedFileSize @ fp+12 (unused)
            int64_t off = i64(fp + 16);
            // i64 shaHashOffset @ fp+24 (unused)
            int64_t aes = i64(fp + 32);
            out.entries.emplace(h, Entry{(uint64_t)off, padded, (uint64_t)aes});
        }
    }
    return true;
}

// 64-bit ER dvdbnd path hash: lowercase, '\' → '/', ensure a leading '/', then
// h = h*0x85 + ch over the whole string.
uint64_t path_hash(const std::string &vpath)
{
    std::string s;
    s.reserve(vpath.size() + 1);
    for (char c : vpath)
        s.push_back(c == '\\' ? '/' : (char)std::tolower((unsigned char)c));
    if (s.empty() || s.front() != '/')
        s.insert(s.begin(), '/');
    uint64_t h = 0;
    for (unsigned char c : s)
        h = h * 0x85u + c;
    return h;
}

