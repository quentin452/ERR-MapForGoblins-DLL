struct ArchiveKey
{
    const char    *name;     // "Data0" → Data0.bhd / Data0.bdt
    const uint8_t *mod;      // 256-byte big-endian modulus
    const uint8_t *exp;      // big-endian public exponent
    uint32_t       explen;
};
