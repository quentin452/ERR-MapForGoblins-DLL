constexpr uint32_t kModLen = 256;  // RSA-2048

// One file's location inside the archive's .bdt.
struct Entry
{
    uint64_t offset = 0;
    uint32_t padded = 0;
    uint64_t aesOff = 0;  // offset into the decrypted BHD5 of this file's AESKey (0 = none)
};
